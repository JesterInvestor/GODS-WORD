// Parse all ranges on module load
const parsedRanges = parseVerseRanges(jesusWordsRanges);

/**
 * Check if a specific verse contains Jesus's words
 * @param book - Book name (e.g., "Matthew", "Mark", "Luke", "John")
 * @param chapter - Chapter number
 * @param verse - Verse number
 * @returns true if the verse contains Jesus's words
 */
export function isJesusWords(book: string, chapter: number, verse: number): boolean {
  return parsedRanges.some(
    range =>
      range.book === book &&
      range.chapter === chapter &&
      verse >= range.startVerse &&
      verse <= range.endVerse
  );
}

/**
 * Check if the entire verse text contains Jesus's words
 * This is the main function used by the rendering component
 */
export function shouldHighlightAsJesusWords(book: string, chapter: string, verse: string): boolean {
  const chapterNum = parseInt(chapter);
  const verseNum = parseInt(verse);

  if (isNaN(chapterNum) || isNaN(verseNum)) {
    return false;
  }

  return isJesusWords(book, chapterNum, verseNum);
}
