// This file defines verse ranges that contain Jesus's words in the Gospels
// Format: "Book Chapter:Verse" or "Book Chapter:StartVerse-EndVerse"
// This is a subset of common passages - a complete implementation would include all Jesus's words

export interface VerseRange {
  book: string;
  chapter: number;
  startVerse: number;
  endVerse: number;
}

// Helper function to parse verse ranges
export function parseVerseRanges(ranges: string[]): VerseRange[] {
  const parsed: VerseRange[] = [];

  for (const range of ranges) {
    const match = range.match(/^(\w+)\s+(\d+):(\d+)(?:-(\d+))?$/);
    if (match) {
      const [, book, chapter, startVerse, endVerse] = match;
      parsed.push({
        book,
        chapter: parseInt(chapter),
        startVerse: parseInt(startVerse),
        endVerse: endVerse ? parseInt(endVerse) : parseInt(startVerse),
      });
    }
  }

  return parsed;
}

